#include <stdio.h>

int main()
{	
	/* declare */
	int v1; int v2; int vsum;
	int all_in_one=5;
	
	/* initialize */
	v1=1;
	v2=2;

	/* compute */
	vsum = v1 + v2;

	/* print result */
	printf("The sum of %i and %i is %i\n",v1,v2,vsum);

	/* display all in one */
	printf("all_in_one => %i\n",all_in_one);
}
